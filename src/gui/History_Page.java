    public void loadHistory() {
        try {
            ResultSet rs = MySQL.search("SELECT * FROM `customer_history` ");
            DefaultTableModel dtm = (DefaultTableModel) jTable4.getModel();
            dtm.setRowCount(0);

            while (rs.next()) {
                Vector v = new Vector();
                v.add(rs.getString("id"));
                v.add(rs.getString("customer_nic"));
                v.add(rs.getString("check_in_date"));
                v.add(rs.getString("check_in_time"));
                v.add(rs.getString("check_out_date"));
                v.add(rs.getString("check_out_time"));
                v.add(rs.getString("room_no"));
                v.add(rs.getString("reseption_hall_no"));
                v.add(rs.getString("pool_no"));
                v.add(rs.getString("bar_no"));

                dtm.addRow(v);
            }
        } catch (Exception e) {
            e.printStackTrace();
        }
    }

