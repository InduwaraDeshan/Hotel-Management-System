    private void jButton9ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton9ActionPerformed
        // TODO add your handling code here:

        String nic = jLabel19.getText();
        String indate = jLabel30.getText();
        String intime = jLabel34.getText();
        String outdate = jLabel20.getText();
        String outtime = jLabel31.getText();
        String room = jLabel33.getText();
        String hall = jLabel29.getText();
        String pool = jLabel32.getText();
        String bar = jLabel35.getText();

        try {
            InputStream stream = getClass().getResourceAsStream("/report/Gm_Customer_History.jrxml");
            JasperReport jr = JasperCompileManager.compileReport(stream);
            HashMap parameters = new HashMap();

            Date d = new Date();
            SimpleDateFormat sdf_time = new SimpleDateFormat("yyyy-MM-dd hh:mm:ss a");

            parameters.put("date", sdf_time.format(d));
            Vector v = new Vector();
            v.add(new History_Bean(nic, indate, intime, outdate, outtime, room, hall, pool, bar));

            JRBeanCollectionDataSource datasource = new JRBeanCollectionDataSource(v);
            JasperPrint jp = JasperFillManager.fillReport(jr, parameters, datasource);
            JasperViewer jv = new JasperViewer(jp, false);
            jv.setVisible(true);

        } catch (Exception e) {
            e.printStackTrace();
        }


    }//GEN-LAST:event_jButton9ActionPerformed

    private void jTable4MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable4MouseClicked
        // TODO add your handling code here:

        if (evt.getClickCount() == 1) {
            int r = jTable4.getSelectedRow();
            if (r == -1) {
                JOptionPane.showMessageDialog(this, "Please select a Row", "Warning", JOptionPane.WARNING_MESSAGE);

            } else {
                int option = JOptionPane.showConfirmDialog(this, "Is The Customer You Selected Correct ?", "Confirmation", JOptionPane.YES_NO_OPTION, JOptionPane.INFORMATION_MESSAGE);
                if (option == JOptionPane.YES_OPTION) {

                    String id = jTable4.getValueAt(r, 0).toString();
                    String nic = jTable4.getValueAt(r, 1).toString();
                    String indate = jTable4.getValueAt(r, 2).toString();
                    String intime = jTable4.getValueAt(r, 3).toString();
                    String outdate = jTable4.getValueAt(r, 4).toString();
                    String outtime = jTable4.getValueAt(r, 5).toString();
                    String rno = jTable4.getValueAt(r, 6).toString();
                    String hno = jTable4.getValueAt(r, 7).toString();
                    String pno = jTable4.getValueAt(r, 8).toString();
                    String bno = jTable4.getValueAt(r, 9).toString();

                    jLabel19.setText(nic);
                    jLabel30.setText(indate);
                    jLabel34.setText(intime);
                    jLabel20.setText(outdate);
                    jLabel31.setText(outtime);
                    jLabel33.setText(rno);
                    jLabel29.setText(hno);
                    jLabel32.setText(pno);
                    jLabel35.setText(bno);
                    this.resize(1319, 1020);

                }
            }
        }
    }//GEN-LAST:event_jTable4MouseClicked

