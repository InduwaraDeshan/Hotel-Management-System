    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        String brand_id = jLabel10.getText();
        String category_id = jLabel11.getText();
        String product_name = jTextField3.getText();

        if (brand_id.equals("None")) {
            JOptionPane.showMessageDialog(this, "Please Select Brand ", "Warning", JOptionPane.WARNING_MESSAGE);
        } else if (category_id.equals("None")) {
            JOptionPane.showMessageDialog(this, "Please Select Category", "Warning", JOptionPane.WARNING_MESSAGE);
        } else if (product_name.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please enter Product Name", "Warning", JOptionPane.WARNING_MESSAGE);

        } else {

            try {
                ResultSet rs = MySQL.search("SELECT * FROM  `product`   WHERE `name`='" + product_name + "' AND `category_id`='" + category_id + "' AND `brand_id`='" + brand_id + "';");

                if (rs.next()) {
                    JOptionPane.showMessageDialog(this, "Product alredy exists", "Warning", JOptionPane.WARNING_MESSAGE);

                } else {

                    MySQL.iud("INSERT INTO `product` (`name`,`brand_id`,`category_id`) VALUES ('" + product_name + "','" + brand_id + "','" + category_id + "')");

                    jTextField3.setText("");
                    jLabel10.setText("None");
                    jLabel11.setText("None");

                    loadProduct();
                    this.resize(1319, 1020);
                    JOptionPane.showMessageDialog(this, "Product Added", "Success", JOptionPane.INFORMATION_MESSAGE);

                }

            } catch (Exception e) {
                e.printStackTrace();
            }

        }
    }//GEN-LAST:event_jButton3ActionPerformed

